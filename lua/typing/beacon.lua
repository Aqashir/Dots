----------------------------------------
--Disable/enable beacon
vim.g.beacon_enable = 1
----------------------------------------

----------------------------------------
--Changing beacon size
vim.g.beacon_size = 40
----------------------------------------

----------------------------------------
--show beacon in all jumps. if it set to zero it will only show when cursor changes windows
vim.g.beacon_show_jumps = 1
----------------------------------------

----------------------------------------
--change what beacon considers significant jump
vim.g.beacon_minimal_jump = 10
----------------------------------------
--disable/enable shrinking animation
vim.g.beacon_shrink = 1
----------------------------------------

----------------------------------------
--disable/enable fading animation
vim.g.beacon_fade = 1
----------------------------------------

----------------------------------------
--set vim.g.beacon_timeout to hide beacon early
vim.g.beacon_timeout = 500 --it's in milliseconds
----------------------------------------

----------------------------------------
--File type to ignore
vim.g.beacon_ignore_filetypes = {''}
----------------------------------------
