vim.g.floaterm_position = 'bottom'
----------------------------------------------------------------------


----------------------------------------------------------------------
--                      Open terminal as split                      --

vim.g.floaterm_opener = 'split'
----------------------------------------------------------------------


----------------------------------------------------------------------
--                   Always close terminal window                   --

vim.g.floaterm_autoclose = 2
----------------------------------------------------------------------


----------------------------------------------------------------------
--                       Always hide previous                       --

vim.g.floaterm_autohide = 2
----------------------------------------------------------------------


----------------------------------------------------------------------
--                          Terminal width                          --

vim.g.floaterm_width = 70
----------------------------------------------------------------------


----------------------------------------------------------------------
--                         Terminal Height                          --

vim.g.floaterm_height = 25
----------------------------------------------------------------------

----------------------------------------------------------------------
--                           Window type                            --

vim.g.floaterm_wintype = 'split'
----------------------------------------------------------------------

----------------------------------------------------------------------
--                Put me in insert mode insistently                 --

vim.g.floaterm_autoinsert = true
----------------------------------------------------------------------

----------------------------------------------------------------------
--                             Keymaps                              --


vim.g.floaterm_keymap_new = '<Leader>fy'
