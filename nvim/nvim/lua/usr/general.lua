local set = vim.o
local setGlobal = vim.g

----------------------------------------------------------------------
--                   auto + smart indent for code                   --

set.autoindent = true
set.smartindent = true
----------------------------------------------------------------------


----------------------------------------------------------------------
--                        => Mouse Scrolling                        --

set.mouse = 'r'
----------------------------------------------------------------------


----------------------------------------------------------------------
--                            Line warp                             --

set.wrap = true
----------------------------------------------------------------------


----------------------------------------------------------------------
--                        Position in code                         --

set.number = false
set.ruler = true
----------------------------------------------------------------------


----------------------------------------------------------------------
--                           Tab related                            --

set.expandtab = true             --Use spaces instead of tabs.
set.smarttab = true              --Be smart using tabs ;)
set.shiftwidth = 4               --One tab == four spaces
set.tabstop = 4
----------------------------------------------------------------------


----------------------------------------------------------------------
--                         No warning sound                         --

set.errorbells = false
----------------------------------------------------------------------


----------------------------------------------------------------------
--            Don't highlight search results by default             --


set.hlsearch = false
----------------------------------------------------------------------


----------------------------------------------------------------------
--          Store backup, undo and swap files in temp dir           --

set.directory = os.getenv('HOME') .. '/tmp/swap'
set.backupdir = os.getenv('HOME') .. '/tmp/backup'
set.undodir = os.getenv('HOME') .. '/tmp/undo'
----------------------------------------------------------------------


----------------------------------------------------------------------
--                             Spelling                             --

set.spell = true
set.spelllang =  'en_us'
----------------------------------------------------------------------


----------------------------------------------------------------------
--                        Global Status Line                        --

set.laststatus = 3
----------------------------------------------------------------------


----------------------------------------------------------------------
--                          Show Tab Line                           --

set.showtabline = 2
----------------------------------------------------------------------


----------------------------------------------------------------------
--                           Number Width                           --

-- set.numberwidth = 1
----------------------------------------------------------------------


----------------------------------------------------------------------
--          We don't need to see things like -- INSERT --           --
--                             anymore                              --

set.showmode = false
----------------------------------------------------------------------


----------------------------------------------------------------------
--            Highlight the text line of the cursor with            --
--                            Cursorline                            --

set.cursorline = true
----------------------------------------------------------------------

----------------------------------------------------------------------
--                         Conceal sittings                         --

set.conceallevel = 2
set.concealcursor = ''
----------------------------------------------------------------------

----------------------------------------------------------------------
--                    Hide cmdline if not needed                    --

-- set.cmdheight = 0 NOTE: Enable it when neovim 8 is Released
----------------------------------------------------------------------


----------------------------------------------------------------------
--                      Cursor Hold Update time                  --

set.updatetime = 1000
----------------------------------------------------------------------


----------------------------------------------------------------------
--                    Enables 24-bit RGB colors                     --

set.termguicolors = true
----------------------------------------------------------------------

----------------------------------------------------------------------
--                        Set graphical font                        --

set.guifont = 'MesloLGS Nerd Font:h13'
----------------------------------------------------------------------

----------------------------------------------------------------------
--                              netrw                               --

setGlobal.loaded = 1
setGlobal.loaded_netrwPlugin = 1
